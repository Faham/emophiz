using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SensorLib.Mindset
{

    public enum BrainBand
    {
        Signal,
        Attention,
        Meditation,
        Raw,
        Delta,
        Theta,
        Alpha1,
        Alpha2,
        Beta1,
        Beta2,
        Gamma1,
        Gamma2
    }


    public class BrainData
    {

        public BrainData()
        {
            return;
        }

        public BrainData(BrainData data)
        {
            signal = data.signal;
            attention = data.attention;
            meditation = data.meditation;
            raw = data.raw;
            delta = data.delta;
            theta = data.theta;
            alpha1 = data.alpha1;
            alpha2 = data.alpha2;
            beta1 = data.beta1;
            beta2 = data.beta2;
            gamma1 = data.gamma1;
            gamma2 = data.gamma2;

            return;
        }

        public float signal;
        public float attention;
        public float meditation;
        public float raw;
        public float delta;
        public float theta;
        public float alpha1;
        public float alpha2;
        public float beta1;
        public float beta2;
        public float gamma1;
        public float gamma2;

    }
}
